# # 자료구조

# ## 배열 Array

# - 삽입/삭제 : O(N), 특정 위치에 값을 넣으려면 나머지 요소 다 밀어줘야 삽입 가능, 삭제도 마찬가지로 하나 지우면 나머지 다 땡겨줘야 삭제 가능. 0~N번 해야하므로 최악을 따라서 O(N)
# - 탐색 : O(1), 임의 접근 가능
# - 파이썬은 리스트 사용
#   arr = [10,11,12,13]

# ## 벡터 Vector (C++에서 많이 쓰임)

# 동적으로 사이즈 바꿀 수 있다는 점 빼고 배열과 비슷
# 파이썬에서는 원래 리스트에서 사이즈 변경 가능해서 크게 다르지 않다.

# - 삽입/삭제 : O(N)
# - 탐색 : O(1)


# 배열(Array) 예시
arr = [10, 11, 12, 13]
print("초기 배열:", arr)

# 삽입 (2번째 인덱스에 99 추가)
arr.insert(2, 99)
print("2번째 인덱스에 99 삽입:", arr)

# 삭제 (3번째 인덱스 값 삭제)
del arr[3]
print("3번째 인덱스 값 삭제:", arr)

# 탐색 (임의 접근)
print("1번째 인덱스 값:", arr[1])

# 벡터(Vector) 예시 (파이썬 리스트와 동일하게 사용)
vector = []
for i in range(5):
    vector.append(i * 10)
print("벡터(리스트) 예시:", vector)

vector.pop()  # 마지막 요소 삭제
print("벡터에서 마지막 요소 삭제:", vector)
